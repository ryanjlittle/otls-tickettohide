template <typename IO>
void test_protocol(IO* io, IO* io_opt, COT<IO>* cot, int num_servers, int party) {
    unsigned char* cmsg = new unsigned char[msg_len];
    if (party == PROVER) {
        memcpy(cmsg, msg, msg_len);
    } else {
        memset(cmsg, 0, msg_len);
    }

    Handshake_13<NetIO>* hs = new Handshake_13<NetIO>(io, io_opt, cot, num_servers);

    auto start = emp::clock_start();

    if (party == VERIFIER) {
        vector<const unsigned char*> hs_secs;
        hs_secs.push_back(hs_sec_1);
        hs_secs.push_back(hs_sec_2);
        hs->set_verifier_handshake_secrets(hs_secs);
    } else {
        hs->set_prover_handshake_secrets(p_index, hash_1);
    }

    hs->compute_handshake_secrets(party);

    // std::cout << "CHTS: ";
    // print_bin_str_as_hex_reversed(hs->chts_revealed);
    // std::cout << std::endl;
    // std::cout << "SHTS: ";
    // print_bin_str_as_hex_reversed(hs->shts_revealed);
    // std::cout << std::endl;

    if (party == VERIFIER) {
        vector<const unsigned char*> master_secs;
        master_secs.push_back(m_sec_1);
        master_secs.push_back(m_sec_2);
        hs->set_verifier_application_secrets(master_secs);
    } else {
        hs->set_prover_application_secrets(hash_2);
    }

    hs->compute_application_keys(party);

    std::cout << "Client key: ";
    print_bin_str_as_hex_reversed(hs->client_write_key.reveal<string>());
    std::cout << "Client IV: ";
    print_bin_str_as_hex_reversed(hs->client_iv_revealed);
    std::cout << "Server key: ";
    print_bin_str_as_hex_reversed(hs->server_write_key.reveal<string>());
    std::cout << "Server IV: ";
    print_bin_str_as_hex_reversed(hs->server_iv_revealed);

    AEAD_13<IO> aead_c = AEAD_13<IO>(io, io_opt, cot, hs->client_write_key, hs->client_write_iv, party);
    //AEAD_13<IO> aead_s = AEAD_13<IO>(io, io_opt, cot, hs->server_write_key, hs->server_write_iv, party);

    // encrypts the query message
    unsigned char* cctxt = new unsigned char[msg_len];
    unsigned char* ctag = new unsigned char[tag_length];

    aead_c.encrypt(
        io,
        cctxt,
        ctag,
        cmsg,
        msg_len,
        aad,
        aad_len);

    cout << "ctxt: ";
    print_as_hex(cctxt, msg_len);
    // for (size_t i = 0; i < msg_len; i++) {
    //     cout << hex << (int) cctxt[i];
    // }
    // cout << endl;
    cout << "tag: ";
    print_as_hex(ctag, tag_length);
    // for (int i = 0; i < 16; i++) {
    //     cout << hex << (int) ctag[i];
    // }
    // cout << endl;
}

int main(int argc, char** argv) {
    int port, party;
    parse_party_and_port(argv, &party, &port);
    NetIO* io_opt = new NetIO(party == PROVER ? nullptr : "127.0.0.1", port + threads);

    NetIO* io[threads];
    BoolIO<NetIO>* ios[threads];
    for (int i = 0; i < threads; i++) {
        io[i] = new NetIO(party == PROVER ? nullptr : "127.0.0.1", port + i);
        ios[i] = new BoolIO<NetIO>(io[i], party == PROVER);
    }

    auto start = emp::clock_start();
    setup_protocol<NetIO>(io[0], ios, threads, party);
    double setup_time = emp::time_from(start);
    cout << "setup time: " << setup_time << " us" << endl;
    auto prot = (PrimusParty<NetIO>*)(ProtocolExecution::prot_exec);
    IKNP<NetIO>* cot = prot->ot;
    test_protocol<NetIO>(io[0], io_opt, cot, num_servers, party);
    cout << "post-setup time: " << emp::time_from(start)-setup_time << " us" << endl;
    cout << "total time: " << emp::time_from(start) << " us" << endl;

    cout << "gc AND gates: " << dec << gc_circ_buf->num_and() << endl;
    cout << "zk AND gates: " << dec << zk_circ_buf->num_and() << endl;
    finalize_protocol();

    bool cheat = CheatRecord::cheated();
    if (cheat)
        error("cheat!\n");

#if defined(__linux__)
    struct rusage rusage;
    if (!getrusage(RUSAGE_SELF, &rusage))
        std::cout << "[Linux]Peak resident set size: " << (size_t)rusage.ru_maxrss
                  << std::endl;
    else
        std::cout << "[Linux]Query RSS failed" << std::endl;
#elif defined(__APPLE__)
    struct mach_task_basic_info info;
    mach_msg_type_number_t count = MACH_TASK_BASIC_INFO_COUNT;
    if (task_info(mach_task_self(), MACH_TASK_BASIC_INFO, (task_info_t)&info, &count) ==
        KERN_SUCCESS)
        std::cout << "[Mac]Peak resident set size: " << (size_t)info.resident_size_max
                  << std::endl;
    else
        std::cout << "[Mac]Query RSS failed" << std::endl;
#endif
    cout << "comm: " << (getComm(io, threads, io_opt) * 1.0) / 1024 << " KBytes" << endl;
    delete io_opt;
    for (int i = 0; i < threads; i++) {
        delete ios[i];
        delete io[i];
    }
    return 0;
}
